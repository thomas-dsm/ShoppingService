package com.shop;

import java.time.LocalDate;

/**
 *
 * @author tdasilvamendonca
 */
public class Book {
    
    private int id;
    private String isbn;
    private String title;
    private String author;
    private LocalDate date;

    public Book() {
    }

    public Book(int id, String isbn, String title, String author, LocalDate date) {
        this.id = id;
        this.isbn = isbn;
        this.title = title;
        this.author = author;
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }
}
